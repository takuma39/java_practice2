package java_project1_section4;

public class calc {

	public static void main(String[] args) {
		// 算術演算子　+ - * %
		int a = 15;
		int b = 4;
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		// %はあまりの数 15÷4=3 余り3
		System.out.println("a % b = " + (a % b));
	}

}
